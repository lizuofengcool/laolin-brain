// ─── GET /api/help/articles/[id] — 获取文档详情 ─────────────
export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const { id } = await params;

    // 查找文档
    const article = HELP_ARTICLES.find((a) => a.id === id);

    if (!article) {
      return NextResponse.json(
        { error: "文档不存在" },
        { status: 404 }
      );
    }

    return NextResponse.json({
      success: true,
      data: article,
    });
  } catch (error) {
    console.error("Failed to fetch help article:", error);
    return NextResponse.json(
      { error: "获取文档详情失败" },
      { status: 500 }
    );
  }
}
